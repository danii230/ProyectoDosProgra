package Domain;

/**
 *
 * @author Michelle
 */
public class Images {

    private String URL;
    private String name;

    public Images(String URL, String name) {
        this.URL = URL;
        this.name = name;
    }

    public String getURL() {
        return URL;
    }

    public void setURL(String URL) {
        this.URL = URL;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
